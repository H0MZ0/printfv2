#include "ft_printf.h"

int	ft_hexalen(unsigned int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (1);
	while (nb != 0)
	{
		i++;
		nb /= 16;
	}
	return (i);
}
